export default class RSA {
  #privateKey;
  constructor() {
    this.publicKey;
  }

  createKeyPair = ([p1, p2]) => {
    return p1 + p2;
  };

  encrypt = (message) => {
    const cipher = message;
    return cipher;
  };

  decrypt = (cipher) => {
    const message = cipher;
    return message;
  };
}
